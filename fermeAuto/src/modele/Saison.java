package modele;

/**
 * -enumération des saisons dans le jeu.
 * -chaque saison dure 30 jours et a des effets spécifiques :
 *  AUTOMNE : beaucoup de pluie
 * ; HIVER   : froid, tomates gelées, production réduite
 * ; PRINTEMPS : reproduction facilitée
 * ; ÉTÉ     : sécheresse, augmentatons des besoins
 */

public enum Saison {
    AUTOMNE, HIVER, PRINTEMPS, ETE;
}